export function atualizarPosicaoBoneco(oldRow, oldColumn, newRow, newColumn) {
    mapa.value[oldRow][oldColumn] = 0;
    mapa.value[newRow][newColumn] = 99;
}

export function verificarItensColetados(itens) {

    for (let i = 0; i < mapa.value.length; i++) {
        for (let j = 0; j < mapa.value[i].length; j++) {
            if (mapa.value[i][j] !== 0) {
                const item = itens.find(item => item.id === mapa.value[i][j]);
                if (item && item.coletado) {
                    mapa.value[i][j] = 0;
                }
            }
        }
    }

}

